export const HAT_LINES: Record<string, string> = {
  none: "", crown: " \\^^^/ ", tophat: " [___] ", propeller: " -+- ",
  halo: " ( ) ", wizard: " /^\\ ", beanie: " (___) ", tinyduck: " ,> ",
};

export function renderSprite(species: string, hat?: string): string[][] {
  const frames = BODIES[species];
  if (!frames) return [["  ???  "]];
  const defaultEye = "·";
  return frames.map(frameLines => {
    const lines = frameLines.map(line => line.replaceAll("{E}", defaultEye));
    if (hat && hat !== "none" && HAT_LINES[hat] && !lines[0].trim()) {
      lines[0] = HAT_LINES[hat];
    }
    return lines;
  });
}
